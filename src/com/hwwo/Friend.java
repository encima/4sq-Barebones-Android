package com.hwwo;

public class Friend {
	
	private String id;
	private String name;
	private String photo;
	private String gender;
	private String city;

	void setID(String id) {
		this.id = id;
	}

	String getID() {
		return id;
	}

	void setName(String name) {
		this.name = name;
	}

	String getName() {
		return name;
	}

	void setPhoto(String photo) {
		this.photo = photo;
	}

	String getPhoto() {
		return photo;
	}

	void setGender(String gender) {
		this.gender = gender;
	}

	String getGender() {
		return gender;
	}

	void setCity(String city) {
		this.city = city;
	}

	String getCity() {
		return city;
	}
	

}
